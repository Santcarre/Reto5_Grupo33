    private void mostrarDatos() {

        conexion objConexion = new conexion();

        try {
            ResultSet resultado = objConexion.consultarRegistro("select m.Nombre_Material, "
                    + "m.Importado, "
                    + "COUNT(*)No_Compras "
                    + "from MaterialConstruccion m "
                    + "join Compra c on "
                    + "m.ID_MaterialConstruccion = c.ID_MaterialConstruccion "
                    + "where m.Importado = 'No' "
                    + "group by c.ID_MaterialConstruccion "
                    + "order by         "
                    + "No_Compras;");

            while (resultado.next()) {
                Object[] datos = {resultado.getString("Nombre_Material"),
                    resultado.getString("Importado"),
                    resultado.getString("No_Compras")};

                modelo.addRow(datos);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

